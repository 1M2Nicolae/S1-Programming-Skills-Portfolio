#include <iostream> // declares objects that control reading input and output
using namespace std; // declare a standard namespace


/* 
    Declare a variable with data type integer. Initialise with a value of 8.
    Declare a variable with data type integer. Initialise with a value of 10.
    Declare a third variable of data type integer that adds the previous variables together.
    Print the value of the sum to the console.
 */


 int main() { // defining the main function
    int number1= 8;  // declaring the first variable and initialise with value of 8
    int number2= 10; // declaring the second variable and initialise with value of 10
    int total= number1 + number2; // declaring the third variable that adds the first and second variables together 
    cout<< total << endl; // console output value




return 0; // program  executed successfully
}
